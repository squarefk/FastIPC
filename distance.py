@ti.func
def EE_type(a0, a1, b0, b1):
    u = a1 - a0
    v = b1 - b0
    w = a0 - b0
    a = u.norm_sqr()
    b = u.dot(v)
    c = v.norm_sqr()
    d = u.dot(w)
    e = v.dot(w)
    D = a * c - b * b
    tD = D
    case = 8
    sN = b * e - c * d
    tN = 0.0
    if sN <= 0.0:
        tN = e
        tD = c
        case = 2
    elif sN >= D:
        tN = e + b
        tD = c
        case = 5
    else:
        tN = a * e - b * d
        if 0.0 < tN < tD and (u.cross(v).dot(w) == 0.0 or u.cross(v).norm_sqr() < 1.0e-20 * a * c):
            if sN < D / 2:
                tN = e
                tD = c
                case = 2
            else:
                tN = e + b
                tD = c
                case = 5
    if tN <= 0.0:
        if -d <= 0.0:
            case = 0
        elif -d >= a:
            case = 3
        else:
            case = 6
    elif tN >= tD:
        if -d + b <= 0.0:
            case = 1
        elif -d + b >= a:
            case = 4
        else:
            case = 7
    return case


